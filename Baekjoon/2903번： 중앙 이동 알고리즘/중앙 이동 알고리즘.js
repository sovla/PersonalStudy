const fs = require("fs");
const input = fs
  .readFileSync("/dev/stdin")
  .toString()
  .trim()
  .split("")
  .map(Number)[0];

//   1.	초기 상태:
//   •	정사각형의 네 꼭짓점이 있으므로, 한 변에는 2개의 점(끝점)이 있습니다.
//   2.	1회 진행 시:
//   •	각 변의 중앙에 점을 추가하면 한 변이 두 구간으로 나뉘게 됩니다.
//   •	구간의 수가 2가 되므로, 한 변에 있는 점의 개수는 구간 수 + 1, 즉 2 + 1 = 3개가 됩니다.
//   •	전체 점의 수는 3×3 = 9개가 됩니다.
//   3.	일반화:
//   •	매 회 진행할 때마다 기존의 각 구간이 두 개로 나뉘므로, 총 구간의 수는 매번 2배가 됩니다.
//   •	N회 진행한 후 한 변의 구간 수는 2^N개가 됩니다.
//   •	따라서 한 변의 점의 개수는 2^N + 1개가 됩니다.
//   4.	최종 답:
//   •	정사각형 격자이므로 전체 점의 수는 (2^N + 1)²가 됩니다.

console.log((2 ** input + 1) ** 2);
